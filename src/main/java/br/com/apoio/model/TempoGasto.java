package br.com.apoio.model;

public class TempoGasto {

	private int tempoGastoComConversao;
	private int tempoGastoSemConversao;
	private int agendamentosGastosComConversao;
	private int agendamentoGastosSemConversao;

	public int getTempoGastoComConversao() {
		return tempoGastoComConversao;
	}

	public void setTempoGastoComConversao(int tempoGastoComConversao) {
		this.tempoGastoComConversao = tempoGastoComConversao;
	}

	public int getTempoGastoSemConversao() {
		return tempoGastoSemConversao;
	}

	public void setTempoGastoSemConversao(int tempoGastoSemConversao) {
		this.tempoGastoSemConversao = tempoGastoSemConversao;
	}

	public int getAgendamentosGastosComConversao() {
		return agendamentosGastosComConversao;
	}

	public void setAgendamentosGastosComConversao(int agendamentosGastosComConversao) {
		this.agendamentosGastosComConversao = agendamentosGastosComConversao;
	}

	public int getAgendamentoGastosSemConversao() {
		return agendamentoGastosSemConversao;
	}

	public void setAgendamentoGastosSemConversao(int agendamentoGastosSemConversao) {
		this.agendamentoGastosSemConversao = agendamentoGastosSemConversao;
	}

}
